namespace program
{
    class Methods
    { 
        public static double answer = 0;
        public static void Add(double a, double b)
        {
            answer =  a + b;
        }
        public static void Sub(double a, double b)
        {
            answer = a - b;
        }
        public static void Mult(double a, double b)
        {
            answer = a * b;
        }
        public static void Div(double a, double b)
        {
            answer = a / b;
        }
    }
}